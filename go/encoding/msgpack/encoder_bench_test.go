type benchEncFn func(b *testing.B, v interface{}, n int)

func benchEnc(b *testing.B, v interface{}, fn benchEncFn) {
	runtime.GC()
	b.ResetTimer()
	var (
		w sync.WaitGroup
		m = runtime.GOMAXPROCS(0)
		n = b.N / m
	)
	w.Add(m)
	for i := 0; i < m; i++ {
		go func() {
			fn(b, v, n)
			w.Done()
		}()
	}
	w.Wait()
}

func benchMsgpack(b *testing.B, v interface{}, n int) {
	h := &codec.MsgpackHandle{}
	for i := 0; i < n; i++ {
		var d []byte
		if err := codec.NewEncoderBytes(&d, h).Encode(v); err != nil {
			b.Log("Error encoding:", err)
			b.FailNow()
		}
	}
}

func benchEncPool(b *testing.B, v interface{}, n int) {
	for i := 0; i < n; i++ {
		if _, err := Marshal(v); err != nil {
			b.Log("Error encoding:", err)
			b.FailNow()
		}
	}
}

func Benchmark__Msgpack__Encode_Small(b *testing.B) {
	benchEnc(b, small, benchMsgpack)
}

func Benchmark__Msgpack__Encode_Large(b *testing.B) {
	benchEnc(b, large, benchMsgpack)
}

func Benchmark__Pool_____Encode_Small(b *testing.B) {
	benchEnc(b, small, benchEncPool)
}

func Benchmark__Pool_____Encode_Large(b *testing.B) {
	benchEnc(b, large, benchEncPool)
}
